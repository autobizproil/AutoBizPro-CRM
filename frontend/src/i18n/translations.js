export function useT() {
  const lang = (() => {
    try {
      const raw = localStorage.getItem('crm_prefs')
      return raw ? (JSON.parse(raw).lang ?? 'he') : 'he'
    } catch {
      return 'he'
    }
  })()
  return (key) => translations[lang]?.[key] ?? translations.he[key] ?? key
}
